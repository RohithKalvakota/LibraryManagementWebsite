<h2 style="color:white"><u>IMPORTANCE OF BOOKS</u></h2>
<p style="align:justify">Books play a significant role in our life. They say that "When you open a book, you open a new world". I believe that everyone would agree with this statement as books have become inevitable to mankind. For the majority of people, books are part of their everyday life.  A book is like a best friend who will never walk away from you.<br><br>Books are packed with knowledge, insights into a happy life, life lessons, love, fear, prayer and helpful advice. One can read about anything under the sun. Books have been here for centuries and without them today's knowledge of our past ancestors, cultures and civilizations would have been impossible. Have you ever thought what would have happened if intellectuals never documented their studies?<br><br>
On the 23rd of April the world celebrates the World Book Day. This day is a celebration of authors, illustrators, books and most importantly reading. The main aim of the World Book Day is to encourage children to the pleasures of books and reading. The reason for choosing this particular date is interesting. The 23rd of April is a symbolic date for world literature because it is the date of death for many great authors and poets such as William Shakespeare, Miguel de Cervantes, William Wordsworth and many others.<br><br>
In my opinion reading is the perfect hobby and I am sure that a lot of people would agree. There are a lot of wonderful reasons why reading is important. Here are a few reasons why you should consider reading a book:<br><br><center>
<img src="book10.jpg" height="60%" width="40%"><br><br></center>
To expose yourself to new thing: Through reading, you expose yourself to new things, new information, new ideas, new ways to solve a problem, and new ways to achieve a goal. Reading might help you discover hobbies or exploring things you didn't know you like. Exploration begins from reading and understanding.<br><br>
To improve oneself: Reading helps you understand the world more. Through it, you begin to have a better understanding on a topic that interest you. Self-improvement start from reading, through reading you have a better understanding and better decisions to take in the future.
To improve your understanding: The more you read the more you understand things completely and helps you find the truth about something. Through reading you learn more about society and how to adapt in it.
To improve your imagination: You are limited by what you can imagine, all the worlds described in books as well as views and opinions by other people, will help you expand your understanding of what is possible.
To reduce stress: Studies show that reading reduces stress. The participants in this study only needed to read silently for a few minutes to slow down the heart and ease tension in the muscles.<br><br>
To improve your memory: When you read you have more to think. Reading gives you a unique pause button for understanding and insight. The benefits of this increased activity helps to keep the memory sharp and the learning capacity agile.
To entertain oneself: Reading has an entertainment value. Reading is not only fun, but it has all the added benefits that we discussed so far. A good book can keep you entertained while developing life skills.<br><br>
